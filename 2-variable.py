# variable adalah sebuah tempat untuk menampung nilai

# String bisa dengan '' atau ""
name = "Fedrian Indra Sasri"
print(type(name))  # Mengecek tipe data dari variable
print("Hello " + name)

firstName = "Fedrian"
lastName = " Indra Sasri"
fullName = firstName + lastName
print("Hello " + fullName)

# Integer type
age = 21
print(type(age))
print("Your age is : " + str(age))

# Floating Point Number
height = 250.5
print(type(height))
print("Your Height is : " + str(height))

# Booelan
human = False
print(type(human))
print("Are you a human : " + str(human))
